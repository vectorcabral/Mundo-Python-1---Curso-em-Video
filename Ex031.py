'''
________________________________________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________________________________

Desenvolva um programa que pergunte a distância em Km. Calcule o preço da passagem, cobrando R$0,50 por Km para viagens 
de até 200 KM e R$0,45 para viagens mais longas.
________________________________________________________________________________________________________________________________________________
________________________________________________________________________________________________________________________________________________
'''
dist = float(input('Qual a distância percorrida em Km? '))
if dist > 200.00:
    print('A sua passagem custará R${:.2f}'.format(dist*0.45))
else:
    print('A sua passagem custará R${:.2f}'.format(dist*0.50))